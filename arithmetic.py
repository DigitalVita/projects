import math

a = int(input('Integer a: '))
b = int(input('Integer b: '))

print(f'Sum: {a + b}')
print(f'Difference: {a - b}')
print(f'Product: {a * b}')
print(f'Quotient: {a / b}')
print(f'Remainder: {a/b - a//b}')
print(f'Logarithm base 10: {math.log10(a)}')
print(f'Int a to the power of int b: {a**b}')